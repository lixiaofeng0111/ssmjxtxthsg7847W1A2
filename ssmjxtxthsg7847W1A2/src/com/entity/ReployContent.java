package com.entity;

public class ReployContent {
	
	private Integer id;
	private Integer userId;
	private String reployContent;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public String getReployContent() {
		return reployContent;
	}
	public void setReployContent(String reployContent) {
		this.reployContent = reployContent;
	}
	

}
